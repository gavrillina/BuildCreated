public class Furneture {
    private String name;
    private int areaFurn;

    public Furneture() {

    }

    public int getArea() {
        return areaFurn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setArea(int area) {
        this.areaFurn = area;
    }
}